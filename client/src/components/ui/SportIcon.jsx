export default function SportIcon({ sport, size = "md", className = "" }) {
  const normalizedSport = sport?.toUpperCase().replace(/\s+/g, "_") || "DEFAULT";
  const icon = sportIcons[normalizedSport] || sportIcons.DEFAULT;
  const color = sportColors[normalizedSport] || sportColors.DEFAULT;

  const sizes = {
    sm: "w-8 h-8 p-1.5",
    md: "w-10 h-10 p-2",
    lg: "w-12 h-12 p-2.5",
  };

  return (
    <div className={`${sizes[size]} ${color} rounded-xl flex-shrink-0 ${className}`}>
      {icon}
    </div>
  );
}

export function getSportColor(sport) {
  const normalizedSport = sport?.toUpperCase().replace(/\s+/g, "_") || "DEFAULT";
  return sportColors[normalizedSport] || sportColors.DEFAULT;
}
